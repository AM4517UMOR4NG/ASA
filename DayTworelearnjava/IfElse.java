package DayTworelearnjava;

public class IfElse {
    public static void main(String[] args) {
        // Deklarasi variabel
        int nilai = 75;//ganti

        // Struktur if-else
        if (nilai >= 80) {
            System.out.println("Nilai Anda A");
        } else if (nilai >= 70) {
            System.out.println("Nilai Anda B");
        } else if (nilai >= 60) {
            System.out.println("Nilai Anda C");
        } else {
            System.out.println("Nilai Anda D");
        }
    }
}